class Empleado {
    constructor(idUsuario, usuario, password, cuil, email, fechaNacimiento, direccion, telefono, legajo, nombre, apellido) {
        this.idUsuario = idUsuario;
        this.usuario = usuario;
        this.password = password;
        this.cuil = cuil;
        this.email = email;
        this.fechaNacimiento = fechaNacimiento;
        this.direccion = direccion;
        this.telefono = telefono;
        this.legajo = legajo;
        this.nombre = nombre;
        this.apellido = apellido;
    }
}